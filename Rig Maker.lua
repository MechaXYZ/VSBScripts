				if (currentAnimKeyframeHandler ~= nil) then
					currentAnimKeyframeHandler:disconnect()
				end

				if (currentAnimTrack ~= nil) then
					currentAnimTrack:Stop()
					currentAnimTrack:Destroy()
					currentAnimTrack = nil
				end

				-- clean up walk if there is one
				if (runAnimKeyframeHandler ~= nil) then
					runAnimKeyframeHandler:disconnect()
				end

				if (runAnimTrack ~= nil) then
					runAnimTrack:Stop()
					runAnimTrack:Destroy()
					runAnimTrack = nil
				end

				return oldAnim
			end

			local function getHeightScale()
				if Humanoid then
					if not Humanoid.AutomaticScalingEnabled then
						return 1
					end

					local scale = Humanoid.HipHeight / HumanoidHipHeight
					if AnimationSpeedDampeningObject == nil then
						AnimationSpeedDampeningObject = nil
					end
					if AnimationSpeedDampeningObject ~= nil then
						scale = 1 + (Humanoid.HipHeight - HumanoidHipHeight) * AnimationSpeedDampeningObject.Value / HumanoidHipHeight
					end
					return scale
				end	
				return 1
			end

			local function rootMotionCompensation(speed)
				local speedScaled = speed * 1.25
				local heightScale = getHeightScale()
				local runSpeed = speedScaled / heightScale
				return runSpeed
			end

			local smallButNotZero = 0.0001
			local function setRunSpeed(speed)
				local normalizedWalkSpeed = 2 -- established empirically using current `913402848` walk animation
				local normalizedRunSpeed  = 3
				local runSpeed = rootMotionCompensation(speed)

				local walkAnimationWeight = smallButNotZero
				local runAnimationWeight = smallButNotZero
				local walkAnimationTimewarp = runSpeed/normalizedWalkSpeed
				local runAnimationTimerwarp = runSpeed/normalizedRunSpeed

				if runSpeed <= normalizedWalkSpeed then
					walkAnimationWeight = 1
				elseif runSpeed < normalizedRunSpeed then
					local fadeInRun = (runSpeed - normalizedWalkSpeed)/(normalizedRunSpeed - normalizedWalkSpeed)
					walkAnimationWeight = 1 - fadeInRun
					runAnimationWeight  = fadeInRun
					walkAnimationTimewarp = 1
					runAnimationTimerwarp = 1
				else
					runAnimationWeight = 1
				end
				currentAnimTrack:AdjustWeight(walkAnimationWeight)
				runAnimTrack:AdjustWeight(runAnimationWeight)
				currentAnimTrack:AdjustSpeed(walkAnimationTimewarp)
				runAnimTrack:AdjustSpeed(runAnimationTimerwarp)
			end

			local function setAnimationSpeed(speed)
				if currentAnim == "walk" then
					setRunSpeed(speed)
				else
					if speed ~= currentAnimSpeed then
						currentAnimSpeed = speed
						currentAnimTrack:AdjustSpeed(currentAnimSpeed)
					end
				end
			end

			local function keyFrameReachedFunc(frameName)
				if (frameName == "End") then
					if currentAnim == "walk" then
						if userNoUpdateOnLoop == true then
							if runAnimTrack.Looped ~= true then
								runAnimTrack.TimePosition = 0.0
							end
							if currentAnimTrack.Looped ~= true then
								currentAnimTrack.TimePosition = 0.0
							end
						else
							runAnimTrack.TimePosition = 0.0
							currentAnimTrack.TimePosition = 0.0
						end
					else
						local repeatAnim = currentAnim
						-- return to idle if finishing an emote
						if (emoteNames[repeatAnim] ~= nil and emoteNames[repeatAnim] == false) then
							repeatAnim = "idle"
						end

						if currentlyPlayingEmote then
							if currentAnimTrack.Looped then
								-- Allow the emote to loop
								return
							end

							repeatAnim = "idle"
							currentlyPlayingEmote = false
						end

						local animSpeed = currentAnimSpeed
						playAnimation(repeatAnim, 0.15, Humanoid)
						setAnimationSpeed(animSpeed)
					end
				end
			end

			local function rollAnimation(animName)
				local roll = math.random(1, animTable[animName].totalWeight) 
				local origRoll = roll
				local idx = 1
				while (roll > animTable[animName][idx].weight) do
					roll = roll - animTable[animName][idx].weight
					idx = idx + 1
				end
				return idx
			end

			local function switchToAnim(anim, animName, transitionTime, humanoid)
				-- switch animation		
				if (anim ~= currentAnimInstance) then

					if (currentAnimTrack ~= nil) then
						currentAnimTrack:Stop(transitionTime)
						currentAnimTrack:Destroy()
					end

					if (runAnimTrack ~= nil) then
						runAnimTrack:Stop(transitionTime)
						runAnimTrack:Destroy()
						if userNoUpdateOnLoop == true then
							runAnimTrack = nil
						end
					end

					currentAnimSpeed = 1.0

					-- load it to the humanoid; get AnimationTrack
					currentAnimTrack = humanoid:LoadAnimation(anim)
					currentAnimTrack.Priority = Enum.AnimationPriority.Core

					-- play the animation
					currentAnimTrack:Play(transitionTime)
					currentAnim = animName
					currentAnimInstance = anim

					-- set up keyframe name triggers
					if (currentAnimKeyframeHandler ~= nil) then
						currentAnimKeyframeHandler:disconnect()
					end
					currentAnimKeyframeHandler = currentAnimTrack.KeyframeReached:connect(keyFrameReachedFunc)

					-- check to see if we need to blend a walk/run animation
					if animName == "walk" then
						local runAnimName = "run"
						local runIdx = rollAnimation(runAnimName)

						runAnimTrack = humanoid:LoadAnimation(animTable[runAnimName][runIdx].anim)
						runAnimTrack.Priority = Enum.AnimationPriority.Core
						runAnimTrack:Play(transitionTime)		

						if (runAnimKeyframeHandler ~= nil) then
							runAnimKeyframeHandler:disconnect()
						end
						runAnimKeyframeHandler = runAnimTrack.KeyframeReached:connect(keyFrameReachedFunc)	
					end
				end
			end

			local function playAnimation(animName, transitionTime, humanoid) 	
				local idx = rollAnimation(animName)
				local anim = animTable[animName][idx].anim

				switchToAnim(anim, animName, transitionTime, humanoid)
				currentlyPlayingEmote = false
			end

			local function playEmote(emoteAnim, transitionTime, humanoid)
				switchToAnim(emoteAnim, emoteAnim.Name, transitionTime, humanoid)
				currentlyPlayingEmote = true
			end

			-------------------------------------------------------------------------------------------
			-------------------------------------------------------------------------------------------

			local toolAnimName = ""
			local toolAnimTrack = nil
			local toolAnimInstance = nil
			local currentToolAnimKeyframeHandler = nil


			local playToolAnimation

			local function toolKeyFrameReachedFunc(frameName)
				if (frameName == "End") then
					playToolAnimation(toolAnimName, 0.0, Humanoid)
				end
			end

			function playToolAnimation(animName, transitionTime, humanoid, priority)	 		
				local idx = rollAnimation(animName)
				local anim = animTable[animName][idx].anim

				if (toolAnimInstance ~= anim) then

					if (toolAnimTrack ~= nil) then
						toolAnimTrack:Stop()
						toolAnimTrack:Destroy()
						transitionTime = 0
					end

					-- load it to the humanoid; get AnimationTrack
					toolAnimTrack = humanoid:LoadAnimation(anim)
					if priority then
						toolAnimTrack.Priority = priority
					end

					-- play the animation
					toolAnimTrack:Play(transitionTime)
					toolAnimName = animName
					toolAnimInstance = anim

					currentToolAnimKeyframeHandler = toolAnimTrack.KeyframeReached:connect(toolKeyFrameReachedFunc)
				end
			end

			local function stopToolAnimations()
				local oldAnim = toolAnimName

				if (currentToolAnimKeyframeHandler ~= nil) then
					currentToolAnimKeyframeHandler:disconnect()
				end

				toolAnimName = ""
				toolAnimInstance = nil
				if (toolAnimTrack ~= nil) then
					toolAnimTrack:Stop()
					toolAnimTrack:Destroy()
					toolAnimTrack = nil
				end

				return oldAnim
			end

			-------------------------------------------------------------------------------------------
			-------------------------------------------------------------------------------------------
			-- STATE CHANGE HANDLERS

			local function onRunning(speed)
				local movedDuringEmote =
					userEmoteToRunThresholdChange and currentlyPlayingEmote and Humanoid.MoveDirection == Vector3.new(0, 0, 0)
				local speedThreshold = movedDuringEmote and Humanoid.WalkSpeed or 0.75
				if speed > speedThreshold then
					local scale = 16.0
					playAnimation("walk", 0.2, Humanoid)
					setAnimationSpeed(speed / scale)
					pose = "Running"
				else
					if emoteNames[currentAnim] == nil and not currentlyPlayingEmote then
						playAnimation("idle", 0.2, Humanoid)
						pose = "Standing"
					end
				end
			end

			local function onDied()
				pose = "Dead"
			end

			local function onJumping()
				playAnimation("jump", 0.1, Humanoid)
				jumpAnimTime = jumpAnimDuration
				pose = "Jumping"
			end

			local function onClimbing(speed)
				local scale = 5.0
				playAnimation("climb", 0.1, Humanoid)
				setAnimationSpeed(speed / scale)
				pose = "Climbing"
			end

			local function onGettingUp()
				pose = "GettingUp"
			end

			local function onFreeFall()
				if (jumpAnimTime <= 0) then
					playAnimation("fall", fallTransitionTime, Humanoid)
				end
				pose = "FreeFall"
			end

			local function onFallingDown()
				pose = "FallingDown"
			end

			local function onSeated()
				pose = "Seated"
			end

			local function onPlatformStanding()
				pose = "PlatformStanding"
			end

			-------------------------------------------------------------------------------------------
			-------------------------------------------------------------------------------------------

			local function onSwimming(speed)
				if speed > 1.00 then
					local scale = 10.0
					playAnimation("swim", 0.4, Humanoid)
					setAnimationSpeed(speed / scale)
					pose = "Swimming"
				else
					playAnimation("swimidle", 0.4, Humanoid)
					pose = "Standing"
				end
			end

			local function animateTool()
				if (toolAnim == "None") then
					playToolAnimation("toolnone", toolTransitionTime, Humanoid, Enum.AnimationPriority.Idle)
					return
				end

				if (toolAnim == "Slash") then
					playToolAnimation("toolslash", 0, Humanoid, Enum.AnimationPriority.Action)
					return
				end

				if (toolAnim == "Lunge") then
					playToolAnimation("toollunge", 0, Humanoid, Enum.AnimationPriority.Action)
					return
				end
			end

			local function getToolAnim(tool)
				for _, c in ipairs(tool:GetChildren()) do
					if c.Name == "toolanim" and c.className == "StringValue" then
						return c
					end
				end
				return nil
			end

			local lastTick = 0

			local function stepAnimate(currentTime)
				local amplitude = 1
				local frequency = 1
				local deltaTime = currentTime - lastTick
				lastTick = currentTime

				local climbFudge = 0
				local setAngles = false

				if (jumpAnimTime > 0) then
					jumpAnimTime = jumpAnimTime - deltaTime
				end

				if (pose == "FreeFall" and jumpAnimTime <= 0) then
					playAnimation("fall", fallTransitionTime, Humanoid)
				elseif (pose == "Seated") then
					playAnimation("sit", 0.5, Humanoid)
					return
				elseif (pose == "Running") then
					playAnimation("walk", 0.2, Humanoid)
				elseif (pose == "Dead" or pose == "GettingUp" or pose == "FallingDown" or pose == "Seated" or pose == "PlatformStanding") then
					stopAllAnimations()
					amplitude = 0.1
					frequency = 1
					setAngles = true
				end

				-- Tool Animation handling
				local tool = Character:FindFirstChildOfClass("Tool")
				if tool and tool:FindFirstChild("Handle") then
					local animStringValueObject = getToolAnim(tool)

					if animStringValueObject then
						toolAnim = animStringValueObject.Value
						-- message recieved, delete StringValue
						animStringValueObject.Parent = nil
						toolAnimTime = currentTime + .3
					end

					if currentTime > toolAnimTime then
						toolAnimTime = 0
						toolAnim = "None"
					end

					animateTool()		
				else
					stopToolAnimations()
					toolAnim = "None"
					toolAnimInstance = nil
					toolAnimTime = 0
				end
			end

			-- connect events
			Humanoid.Died:connect(onDied)
			Humanoid.Running:connect(onRunning)
			Humanoid.Jumping:connect(onJumping)
			Humanoid.Climbing:connect(onClimbing)
			Humanoid.GettingUp:connect(onGettingUp)
			Humanoid.FreeFalling:connect(onFreeFall)
			Humanoid.FallingDown:connect(onFallingDown)
			Humanoid.Seated:connect(onSeated)
			Humanoid.PlatformStanding:connect(onPlatformStanding)
			Humanoid.Swimming:connect(onSwimming)

			-- setup emote chat hook

			-- emote bindable hook

			if Character.Parent ~= nil then
				-- initialize to idle
				playAnimation("idle", 0.1, Humanoid)
				pose = "Standing"
			end

			-- loop to handle timed state transitions and tool animations
			while Character.Parent ~= nil do
				local _, currentGameTime = wait(0.1)
				stepAnimate(currentGameTime)
			end
		end)
	end
end

return tab
